#!/bin/sh

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –∫–æ–º–º–∏—Ç–∞ –∏ –ø—É—à–∞ —Å –ø—Ä–æ–≤–µ—Ä–∫–æ–π
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./scripts/push.sh "—Å–æ–æ–±—â–µ–Ω–∏–µ –∫–æ–º–º–∏—Ç–∞"

set -e

# –ü–µ—Ä–µ—Ö–æ–¥–∏–º –≤ –∫–æ—Ä–µ–Ω—å —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è
cd "$(git rev-parse --show-toplevel)" || exit 1

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Å–æ–æ–±—â–µ–Ω–∏—è –∫–æ–º–º–∏—Ç–∞
if [ -z "$1" ]; then
  echo "‚ùå –û—à–∏–±–∫–∞: –Ω–µ —É–∫–∞–∑–∞–Ω–æ —Å–æ–æ–±—â–µ–Ω–∏–µ –∫–æ–º–º–∏—Ç–∞"
  echo "–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./scripts/push.sh \"—Å–æ–æ–±—â–µ–Ω–∏–µ –∫–æ–º–º–∏—Ç–∞\""
  exit 1
fi

COMMIT_MESSAGE="$1"

echo "üöÄ –ù–∞—á–∞–ª–æ –ø—Ä–æ—Ü–µ—Å—Å–∞ –∫–æ–º–º–∏—Ç–∞ –∏ –ø—É—à–∞..."
echo ""

# –ü–æ–ª—É—á–∞–µ–º —Å–ø–∏—Å–æ–∫ –∏–∑–º–µ–Ω–µ–Ω–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤
CHANGED_FILES=$(git status --porcelain | awk '{print $2}')

if [ -z "$CHANGED_FILES" ]; then
  echo "‚ÑπÔ∏è  –ù–µ—Ç –∏–∑–º–µ–Ω–µ–Ω–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤ –¥–ª—è –∫–æ–º–º–∏—Ç–∞."
  exit 0
fi

echo "üìù –ò–∑–º–µ–Ω–µ–Ω–Ω—ã–µ —Ñ–∞–π–ª—ã:"
echo "$CHANGED_FILES" | sed 's/^/  - /'
echo ""

# –ü–æ–ª—É—á–∞–µ–º —Å–ø–∏—Å–æ–∫ —Ñ–∞–π–ª–æ–≤ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
FILES_TO_CHECK=$(echo "$CHANGED_FILES" | grep -E '\.(js|ts|vue|scss|css)$' || true)

if [ -n "$FILES_TO_CHECK" ]; then
  echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –∏–∑–º–µ–Ω–µ–Ω–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤ –ª–∏–Ω—Ç–µ—Ä–æ–º..."
  echo ""
  
  # –ü—Ä–æ–≤–µ—Ä—è–µ–º JavaScript/TypeScript/Vue —Ñ–∞–π–ª—ã
  JS_FILES=$(echo "$FILES_TO_CHECK" | grep -E '\.(js|ts|vue)$' || true)
  if [ -n "$JS_FILES" ]; then
    echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ ESLint..."
    echo "$JS_FILES" | xargs npm run lint || {
      echo ""
      echo "‚ùå –û—à–∏–±–∫–∏ ESLint –æ–±–Ω–∞—Ä—É–∂–µ–Ω—ã!"
      echo "–ü–æ–ø—ã—Ç–∫–∞ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏—è..."
      echo "$JS_FILES" | xargs npm run lint:fix
      
      # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–Ω–æ–≤–∞ –ø–æ—Å–ª–µ –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏—è
      echo "$JS_FILES" | xargs npm run lint || {
        echo ""
        echo "‚ùå –û—à–∏–±–∫–∏ ESLint –Ω–µ —É–¥–∞–ª–æ—Å—å –∏—Å–ø—Ä–∞–≤–∏—Ç—å –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏!"
        echo "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∏—Å–ø—Ä–∞–≤—å—Ç–µ –æ—à–∏–±–∫–∏ –≤—Ä—É—á–Ω—É—é."
        exit 1
      }
      
      echo "‚úÖ –û—à–∏–±–∫–∏ ESLint –∏—Å–ø—Ä–∞–≤–ª–µ–Ω—ã –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏."
    }
  fi
  
  # –ü—Ä–æ–≤–µ—Ä—è–µ–º CSS/SCSS —Ñ–∞–π–ª—ã
  CSS_FILES=$(echo "$FILES_TO_CHECK" | grep -E '\.(css|scss)$' || true)
  if [ -n "$CSS_FILES" ]; then
    echo ""
    echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ Stylelint..."
    echo "$CSS_FILES" | xargs npm run stylelint || {
      echo ""
      echo "‚ùå –û—à–∏–±–∫–∏ Stylelint –æ–±–Ω–∞—Ä—É–∂–µ–Ω—ã!"
      echo "–ü–æ–ø—ã—Ç–∫–∞ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏—è..."
      echo "$CSS_FILES" | xargs npm run stylelint:fix
      
      # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–Ω–æ–≤–∞ –ø–æ—Å–ª–µ –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏—è
      echo "$CSS_FILES" | xargs npm run stylelint || {
        echo ""
        echo "‚ùå –û—à–∏–±–∫–∏ Stylelint –Ω–µ —É–¥–∞–ª–æ—Å—å –∏—Å–ø—Ä–∞–≤–∏—Ç—å –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏!"
        echo "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∏—Å–ø—Ä–∞–≤—å—Ç–µ –æ—à–∏–±–∫–∏ –≤—Ä—É—á–Ω—É—é."
        exit 1
      }
      
      echo "‚úÖ –û—à–∏–±–∫–∏ Stylelint –∏—Å–ø—Ä–∞–≤–ª–µ–Ω—ã –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏."
    }
  fi
  
  # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º —Ñ–∞–π–ª—ã —Å –ø–æ–º–æ—â—å—é Prettier
  echo ""
  echo "üé® –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Ñ–∞–π–ª–æ–≤ —Å –ø–æ–º–æ—â—å—é Prettier..."
  echo "$FILES_TO_CHECK" | xargs npm run format
fi

echo ""
echo "‚úÖ –í—Å–µ –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø—Ä–æ–π–¥–µ–Ω—ã —É—Å–ø–µ—à–Ω–æ!"
echo ""

# –î–æ–±–∞–≤–ª—è–µ–º –≤—Å–µ –∏–∑–º–µ–Ω–µ–Ω–Ω—ã–µ —Ñ–∞–π–ª—ã
echo "üì¶ –î–æ–±–∞–≤–ª–µ–Ω–∏–µ —Ñ–∞–π–ª–æ–≤ –≤ staging area..."
git add -A

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —á—Ç–æ –∫–æ–º–º–∏—Ç–∏—Ç—å
if git diff --cached --quiet; then
  echo "‚ÑπÔ∏è  –ù–µ—Ç –∏–∑–º–µ–Ω–µ–Ω–∏–π –¥–ª—è –∫–æ–º–º–∏—Ç–∞ (–≤–æ–∑–º–æ–∂–Ω–æ, –≤—Å–µ —É–∂–µ –∑–∞–∫–æ–º–º–∏—á–µ–Ω–æ)."
  exit 0
fi

# –°–æ–∑–¥–∞–µ–º –∫–æ–º–º–∏—Ç
echo "üíæ –°–æ–∑–¥–∞–Ω–∏–µ –∫–æ–º–º–∏—Ç–∞..."
git commit -m "$COMMIT_MESSAGE"

# –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Ç–µ–∫—É—â–µ–π –≤–µ—Ç–∫–µ –∏ —É–¥–∞–ª–µ–Ω–Ω–æ–º —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–∏
CURRENT_BRANCH=$(git branch --show-current)
REMOTE=$(git remote | head -n 1)

if [ -z "$REMOTE" ]; then
  echo "‚ö†Ô∏è  –£–¥–∞–ª–µ–Ω–Ω—ã–π —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω. –ö–æ–º–º–∏—Ç —Å–æ–∑–¥–∞–Ω, –Ω–æ –ø—É—à –Ω–µ –≤—ã–ø–æ–ª–Ω–µ–Ω."
  exit 0
fi

echo ""
echo "üì§ –û—Ç–ø—Ä–∞–≤–∫–∞ –∏–∑–º–µ–Ω–µ–Ω–∏–π –≤ —É–¥–∞–ª–µ–Ω–Ω—ã–π —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π..."
echo "–í–µ—Ç–∫–∞: $CURRENT_BRANCH"
echo "–£–¥–∞–ª–µ–Ω–Ω—ã–π —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π: $REMOTE"

# –í—ã–ø–æ–ª–Ω—è–µ–º push (pre-push hook –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –∑–∞–ø—É—Å—Ç–∏—Ç—Å—è)
git push "$REMOTE" "$CURRENT_BRANCH"

echo ""
echo "‚úÖ –ò–∑–º–µ–Ω–µ–Ω–∏—è —É—Å–ø–µ—à–Ω–æ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω—ã –≤ —É–¥–∞–ª–µ–Ω–Ω—ã–π —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π!"
